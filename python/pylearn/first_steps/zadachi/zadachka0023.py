# Задачи А.  14) Пользователь вводит два числа.  Найдите сумму и
# произведение данных чисел.  15) Пользователь вводит число.  Выведите
# на экран квадрат этого числа, куб этого числа.  16) Пользователь
# вводит три числа.  Увеличьте первое число в два раза, второе числа
# уменьшите на 3, третье число возведите в квадрат и затем найдите сумму
# новых трех чисел.  17) Пользователь вводит три числа.  Найдите среднее
# арифметическое этих чисел, а также разность удвоенной суммы первого
# и третьего чисел и утроенного второго числа.


def z23_1_2_3_4():
    x = int(input("Введите первое число: "))
    y = int(input("Введите второе число: "))
    z = int(input("Введите третье число: "))
    a14 = "14) " + str(sum([x, y, z])) + '\t' + str(x*y*z) + '\n' + "15) "
    a15 = x**2, y**2, z**2, x**3, y**3, z**3
    a16 = '\n' + "16) " + str(sum([x*2, y-3, z**2])) + '\n'
    a17 = "17) " + str((x+y+z)/3) + '\t' + str(2*(x+z) - 3*y) + '\n'
    return a14 + ', '.join(str(i) for i in a15) + a16 + a17


print(z23_1_2_3_4())


