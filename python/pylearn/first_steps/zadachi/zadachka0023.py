# Задача С.  Пользователь вводит цены 1 кг конфет и 1 кг печенья.
# Найдите стоимость: а) одной покупки из 300 г конфет и 400 г печенья;
# б) трех покупок, каждая из 2 кг печенья и 1 кг 800 г конфет.


def z23_6():
    x = int(input("Введите цену 1 кг конфет: "))
    y = int(input("Введите цену 1 кг печенья: "))
    a = x * 0.3 + y * 0.4
    b = 3 * (2 * y + 1.8 * x)
    return "19-a) " + str(a) + '\t19-b) ' + str(b)


print(z23_6())


# Задача D.  Пользователь вводит время в минутах и расстояние в
# километрах.  Найдите скорость в м/c.

def z23_7():
    x = int(input("Введите время в минутах: "))
    y = int(input("Введите расстояние в километрах: "))
    return "20) Cкорость: " + str(round((y * 1000) / (x * 60), 2)) + " м/c."


print(z23_7())
