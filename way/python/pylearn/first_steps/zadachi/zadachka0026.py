# Задача 27.  Даны три переменные a, b и c.  Изменить значения этих
# переменных так, чтобы в a хранилось значение a+b, в b хранилась
# разность старых значений c−a, а в c хранилось сумма старых значений
# a+b+c.  Например, a=0, b=2, c=5, тогда новые значения a=2, b=3 и c=7.


def z27(a, b, c):
    a = a + b
    c = a + c
    b = c - 2 * a + b
    return a, b, c


print(z27(3, 50, 100))
