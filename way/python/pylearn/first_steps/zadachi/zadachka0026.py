# Задачи 21.  Даны катеты прямоугольного треугольника.  Найдите площадь,
# периметр и гипотенузу треугольника.


def z21(x, y):
    hyp = (x**2 + y**2)**0.5
    p = x + y + hyp
    s = x * y / 2
    return hyp, p, s


print(z21(3, 4))

